export function getModelPrice(modelId: string, displayName: string): { in: string, out: string } | null {
  const id = modelId.toLowerCase();
  const name = displayName.toLowerCase();
  
  // Try exact match first
  if (MODEL_PRICING[id]) return MODEL_PRICING[id];
  
  // Fuzzy match
  for (const key of Object.keys(MODEL_PRICING)) {
    const cleanKey = key.split('/').pop() || key; // e.g. deepseek/deepseek-v3.2 -> deepseek-v3.2
    if (id.includes(cleanKey) || name.includes(cleanKey)) {
        return MODEL_PRICING[key];
    }
  }
  
  // Custom Doubao backward compatibility (API doubao-pro-32k -> excel doubao-1.5-pro-32k)
  if (id.includes('doubao') && id.includes('pro') && id.includes('32k')) return MODEL_PRICING['doubao-1.5-pro-32k'];
  if (id.includes('doubao') && id.includes('pro') && id.includes('256k')) return MODEL_PRICING['doubao-1.5-pro-256k'] || MODEL_PRICING['doubao-1.5-pro-32k'];
  if (id.includes('doubao') && id.includes('lite') && id.includes('32k')) return MODEL_PRICING['doubao-1.5-lite-32k'];
  
  return null;
}
